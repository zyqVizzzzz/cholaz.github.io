      一炷香后，白伊蔚死透了，殷伏黎却从梁上掉了下来。她没死，在地上晕头转向好一会儿，终于确认自己还活着——
    </p>
    <h3>师娘的猎物</h3>
    <p>虞妙哉指着殷伏黎说：你骗了她。</p>
    <p>
      殷伏黎说：苍天在上，我绝没有，你尽可以去鹩儿巷打听，我殷伏黎是什么人，如果我有骗她的想法，我何必冒险救她呢？虞妙哉说：你还活着，就证明你在骗她。殷伏黎说：我只是不敢再死一次了，死太难受了。
    </p>
    <p>
      说得通，但虞妙哉隐隐觉得不对劲，却一时想不出问题在哪，就抛出一个新问题：你偷走我的白符没几天，它就派上用场了，世上会有这样的巧合？殷伏黎说：这不是巧合，我不止有一张白符。
    </p>
    <p>虞妙哉说：哦？</p>
    <p>
      殷伏黎说：你说的白符，陆小乙家里就有，他不知道这些东西是做什么用的，就被我拿走了，我只知道这些是符，但绝不知道是司天台的符。后来在四通市听梅花道长吹牛，才知道它的来头，前些天我在你的床下发现同样的符纸，实在难忍好奇，所以才偷偷拿走了几张。
    </p>
    <p>虞妙哉说：他家里除了白符，还有其他的符吗。</p>
    <p>
      殷伏黎说：没有了，我们是在很多年前，整理他爸爸遗物时发现的，那时我和沈纨在典当铺工作，对这些东西很感兴趣，就拿回去了<span
        class="tip"
        >（这句话是明显的谎言，因为她刚认识陆小乙没多久，但虞妙哉不知道）</span
      >。
    </p>
    <p>虞妙哉说：你可以随意使用司天台的符咒？</p>
    <p>殷伏黎说：你的符不能，但陆小乙的符，念咒就有用。</p>
    <p>
      虞妙哉知道陆小乙的爸爸是陆科，是发明司天台飞符和白符的传奇人物，他的遗物里有白符是合理的，他的白符比普通的白符更有效也是合理的。
    </p>
    <p>虞妙哉说：除了你，你的那个同伙也知道白符的事情吗？</p>
    <p>殷伏黎说：沈纨，他不知道，他只是个收当的，对方术一窍不通。</p>
    <p>虞妙哉说：听起来你很精通？</p>
    <p>殷伏黎说：只是道听途说，学了一点小伎俩，今天在姐姐这里我才算开了眼。</p>
    <p>
      虞妙哉想了想，说：殷姑娘，许无垠的事情我可以暂时替你保密。但你要记得，这是你欠我的债，要还的。
    </p>
    <p>
      殷伏黎拜倒在地，千恩万谢：唯虞师娘马首是瞻。<span class="tip"
        >（师娘是虞家家奴对虞妙哉的特殊称呼，被殷伏黎学到了）</span
      >
    </p>
    <p>
      殷伏黎虽然此刻极尽卑顺，但虞妙哉知道她和其他家奴不同，她是游民、小偷、杀人犯、教唆他人自杀的骗子，虞妙哉甚至能嗅到她在卑顺里暗藏的愉悦，是一种危险的愉悦，可她越是矛盾，虞妙哉就越觉得兴奋，就像面对一种从未尝试过的丹药。虞妙哉现在对她有了全新的认识。
    </p>
    <p>
      虞妙哉很自然地进入角色，用脚轻轻抵住殷伏黎的额头，缓缓向上推，直到两人四目相对，虞妙哉说：明天我要去你的住处看看，你一定还有其他秘密。
    </p>
