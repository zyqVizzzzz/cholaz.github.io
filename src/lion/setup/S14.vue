        >（火道有三个原型，法术原型是萨满教，教义原型是摩尼教，组织架构原型是天师道及后世秘密宗教）</span
      >。摩尼教的世界观和佛教类似，以因果报应为主，但对于穆千穗来说，这里有一个更具吸引力的世界观：男女平等。
    </p>
    <p>
      在古代，男女平等在主流社会是不可想象的。但秘密宗教是异端，这里的世界观和主流社会完全不同，由于底层女性是他们最重要的吸收对象，他们必须对女性予以与传统观念截然不同的看法。在火道的教义里：男女本来就是平等的，男尊女卑、男女大防、夫权在上统统都是邪恶。
    </p>
    <p>
