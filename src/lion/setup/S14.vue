        >（被囚禁在洛阳武库的九黎神，司天台的成立就是为了对抗九黎神）</span
      >。在教会的起源故事里：三位女神会在四十五日大劫到来之时拯救火神。女神拯救男神，也就是说，女性在这场劫数里才是主角，不是火神的附庸，女性可以自己拯救自己。这非常重要，因为三弥神代表的是女性对自我、对人格意识的某种憧憬。
    </p>
