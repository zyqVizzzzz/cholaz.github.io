    </p>
    <p>
      陈谊、吕狄和曹钉筋都在刑房工作。曹钉筋是主簿，陈谊是书吏，他们的名字都被记录在洛阳府的正式吏役名册上。吕狄是陈谊招募的仵作，属于「帮书」，是临时工里的临时工。陈谊的地位不如曹钉筋，曹钉筋是在狱长身边工作的秘书，陈谊只是个普通书吏。但陈谊又深得曹律赏识，他是刑房里唯一拥有办案权的书吏。
