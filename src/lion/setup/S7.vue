        class="tip"
        >（曹芳弟弟就是当年帮助陆小乙绑架考官的曹钉筋，他混黑道是因为他是个私生子，在家族里没有地位。后来曹芳爸爸做了洛阳府府丞，曹钉筋就离开了帮派，在洛阳府刑房当了狱长，典狱长也是吏，不是官员编制）</span
      >。原来，曹芳早就知道吴坦尔和宁蓉的事情了，只是一直没有撕破脸。
