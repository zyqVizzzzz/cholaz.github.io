    <p>
      吴坦尔是热衷于炒作的清谈名士，虽才疏学浅，却凭着一张标准的善人面孔和各种人设炒作，在名士界混得风生水起。他经营的人设有：爱妻人设、孝子人设，这两种人设要求他在道德方面无懈可击<span
        class="tip"
        >（他的工作就是讲一些劝人向善的人生感悟）</span
      >。
