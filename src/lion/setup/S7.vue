    </p>
    <p>
      陈谊说：虽然宁蓉伤情严重，毁容、骨折、眼球破损，但杨晓柔会以宁蓉有错在先为由，为自己辩解。现实是，对一些人来说，法律是定罪的工具，对另一些人来说，是脱罪的工具。宁大人，你不会真的以为洛阳府能帮你解决问题吧<span
        class="tip"
        >（宁越是典农署的郎中，虽是正式官员，但属于无权无势的「流外官员」）</span
      >。
    </p>
    <p>
      宁越不甘心，又去吴家讨说法，可他根本进不去吴家所住的社区，因为这里是士族社区。他就在坊门外坐等吴大人的公车，吴大人被他拦了两次，都没有下车见他。第二天，宁越去上班，典农署的领导找他谈话，话里话外劝他不要太较劲，不要到最后连自己的工作都保不住了。
    </p>
    <p>
      晚上，宁越回家，杨雄把他堵在巷子里，要给他几锭银子，作为宁蓉的医药费。还警告他不要把事情闹大，否则宁家在洛阳不会好过。宁越拒收这笔银子，杨雄说：那你今天别想出这个巷子。双方僵持不下，里正过来圆场，劝宁越现实一点：人家给你钱你不要，你到底要讨什么公道，你想要什么结果呢？
    </p>
    <p>
      宁越被迫收下了这笔钱。第二天，洛阳府官差来查抄了宁家，以收受贿赂为由，逮捕了宁越。
    </p>
    <p>当天夜里，宁蓉烧炭自杀。</p>
    <h3>狮山笑话王</h3>
    <p>吴坦尔最终还是加盟了陆小乙的狮山云集，宣布要和过去的自己一刀两断。</p>
    <p>
      他在清谈表演中声泪俱下，情感真挚，虽然观众反应不算理想，但陆小乙当真了，他拉着吴坦尔的手问：你知道人们再提起你的时候，会想到什么吗？
    </p>
    <p>吴坦尔想了想，说：一个勇敢的人。</p>
    <p>陆小乙说：一个道德败坏的人。但是，欢迎加入狮山云集。</p>
    <p>
      接下来的几场演出，吴坦尔不断解构他的过往事迹：我有个朋友，叫王瓒，他父亲死的时候我就像死了亲爹一样难过，我不得不这样，因为人们觉得我一定会这样，我必须要像死了亲爹一样难过。可是葬礼当天，我一点都难过不起来，因为王瓒的哭声竟然像驴叫一样滑稽，我实在忍不住了，只能靠放声大哭来掩饰。那天，人们以为我在哭，其实我是在笑。
    </p>
    <p>他凭借此类自爆风格的地狱笑话成功翻红。</p>
    <p>
      一天，他讲到了宁蓉，讲宁蓉如何勾引他，他又如何靠着一颗正直的心将其化解。最后他隔空向妻子表白：我以前说过很多言不由衷的话，可唯独对我妻子的爱是真的。
    </p>
    <p>人们纷纷为他鼓掌。</p>
    <p>
      突然，有个莽夫指着吴坦尔的鼻子骂道：虚伪！是陈谊的搭档，吕狄。陈谊连忙按下他的脑袋，对大家说：这是傻瓜吕庸泰，别和他计较。人们哄堂大笑。
    </p>
    <h3>天命</h3>
    <p>
      几天后，人们在郊外城隍庙里发现了吴坦尔的尸体。尸体旁边还有一把烧成灰的菟丝草，墙上写着一封血书：
    </p>
    <p>草木蔓兮菟萃生，恒依缠兮死不离，犹似天命兮同飘逸。</p>
    <p>
      宁蓉的遭遇随着吴坦尔的死被公之于众，人们这才知道吴坦尔的真面目。而焚烧菟丝草的威力终于彻底爆发。
    </p>
    <p>
      短时间内，洛阳出现了数十起模仿犯罪，每个凶手都在现场留下了象征着反抗的、燃烧殆尽的菟丝草，和那三行魔咒般的诗。
    </p>
    <p>
      所有人都变得神经兮兮的，一旦有谁怀疑自己的妻女参与了瞳朦子活动，轻则禁闭家中，重则殴打、甚至痛下杀手。形势迅速恶化，不仅游民社区变成了修罗场，戒备森严的庄园也出现了多起客女弑主的谋杀案件。大理寺和司天台等高级办案机构也相继介入了调查<span
        class="tip"
        >（司天台的出现，意味着洛阳连日来的暴力事件被定性为了「巫术犯罪事件」）</span
      >。
    </p>
    <p>
      又一天，陆小乙突然听说妹妹陆山黛被洛阳府带走了，原因是她给吴人坊王员外家的马下毒<span
        class="tip"
        >（陆家和王家有点矛盾，王家这些年扩建住宅，想要侵占陆家的地，常找陆小乙的麻烦）</span
      >。陆小乙立即去找陈谊帮忙，陈谊说：你妹妹没事，她只是给王家的马喂了点泻药，不是毒药。现在她一个人在院里，我们给了她一些吃的，但暂时不能让她走。我带你先去找王家谈谈，给点赔偿，道个歉，争取和解。
    </p>
    <p>
      陆小乙忙了一天，总算把事情解决了。晚上，两人回到家里，沈纨和齐武子正在等着兄妹俩，他们听说陆山黛被捕了，以为发生了什么大事。看到他们平安回来，才松了口气。陆小乙说：晚上留下来吃饭吧，我买了酒。
    </p>
    <p>沈纨说：我们就是奔着这顿酒来的。</p>
    <p>齐武子问沈纨：伏黎妹妹哪去了，刚刚还在的。</p>
    <p>朋友们一边喝酒一边聊天，一边调侃陆山黛，气氛很和谐。</p>
    <p>
      三旬酒后，洛阳府的官差闯进陆家，带头的队长手里拿着一份名单：陆小乙在哪。陆小乙站起来说：是我，怎么了。队长看了一眼他，没有回答，继续叫名字：沈纨？沈纨也站了起来。队长继续念名单：还有一个，殷伏黎。没人回答。队长看了一眼陆黛，问：你叫什么？陆山黛说：陆山黛。陆小乙说：这个是我妹妹。队长对左右官差说：去里面看看。
    </p>
    <p>孟依兰从屋里走出来，问：他们怎么了？</p>
    <p>队长说：去了就知道了。</p>
    <p>官差从屋里出来：里面没人。</p>
    <p>队长说：先把他们两个带走。</p>
    <p>
      次日，殷伏黎的通缉告示贴满了四夷坊。孟依兰打听到，是狮山云集出事了，所有和狮山云集有关的人都在昨晚被抓走了。陆小乙和沈纨作为组织者，被认定成了犯罪首脑。
    </p>
    <h3>补充：门客团的故事</h3>
