    </p>
    <p>
      这句话刺痛了吴坦尔，他陷入沉默。他对妻子几乎没有感情，而他的爱妻孝子人设让他成名的同时，也让他在家里总是处于弱势的地位，他既是妈宝男，又是妻管严。他做梦都想要脱离这种人设，但这很可能会让他付出更大的代价。
    </p>
    <p>
