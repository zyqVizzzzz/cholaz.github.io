    <p>
      鲁褒的「钱神赋」让陆小乙主办的「狮山云集」名声大噪，成了洛阳城里最受欢迎的清谈综艺，尽管这档节目屡遭打压，但热度持续不减。吴坦尔作为脱口秀演员，自然也想在这场热潮中获得关注。
    </p>
