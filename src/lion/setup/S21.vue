      殷伏黎虽然此刻极尽卑顺，但虞妙典知道她和其他家奴不同，她是游民、小偷、杀人犯、教唆他人自杀的骗子，虞妙典甚至能嗅到她在卑顺里暗藏的愉悦，是一种危险的愉悦，可她越是矛盾，虞妙典就越觉得兴奋，就像面对一种从未尝试过的丹药。虞妙典现在对她有了全新的认识。
    </p>
    <p>
      虞妙典很自然地进入角色，用脚轻轻抵住殷伏黎的额头，缓缓向上推，直到两人四目相对，虞妙典说：明天我要去你的住处看看，你一定还有其他秘密。
