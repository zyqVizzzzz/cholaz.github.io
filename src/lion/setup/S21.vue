    <p>上个月，某天夜里。</p><p>两名护院禀告虞妙典，说抓到一个女贼。虞妙典前去查看，见一女子，身材娇小，便把她带入书房，问：你叫什么名字？</p><p>姑娘自报家门：四通市殷伏黎。虞妙典问：来我家所为何事？殷伏黎说：取回我的东西。虞妙典问：什么东西？殷伏黎说：野火朝仙图。</p><p>虞妙典说：你是陆小乙的朋友吗，我好像见过你。</p><p>殷伏黎说：陆小乙不是我的朋友，陆小乙是我的狗。</p><p>虞妙典说：别和我装腔作势，老实点。</p><p>殷伏黎说：把我的东西给我，我即刻便走。</p><p>虞妙典说：我没听说过野火朝仙图。</p><p>殷伏黎说：就在你这里，陆小乙说他给捕风馆送了两幅画，一幅水府流云图，一幅醉客寻山图。可是他搞错了，他把野火朝仙图当成醉客寻山图送出去了。所以野火朝仙图是我的，还给我。</p><p>虞妙典说：怎么，陆小乙连自己的画都分不清吗？</p><p>殷伏黎说：他有脑疾。</p><p>虞妙典找到醉客寻山图，在书案上展开，问：这是你说的野火朝仙图吗？</p><p>殷伏黎说：不错。</p><p>虞妙典说：那你给我讲讲，此画所绘何物，你若能道出一二，我就信你。</p><p>殷伏黎说：难道你没看出来吗？画上的人就是我啊。</p><p>虞妙典说：可是画上没有人。</p><p>殷伏黎说：怎么没有？</p><p>她也不客气，径直上前，坐在虞妙典旁边，不知是不是有意为之，她几乎坐进了虞妙典的怀里，两人紧紧贴着，虞妙典甚至能数清她耳朵后面的发丝，殷伏黎回头看着虞妙典，近乎挑逗地问：想知道我在哪里吗？</p><p>虞妙典又数了数她眼睛上的睫毛：你说吧。</p><p>殷伏黎说：这是一幅仕女图，想必你也知道，陆小乙画画喜欢藏东西，藏在所谓的画境里——</p><p>虞妙典此时也有点心猿意马，她已经不想知道野火朝仙图里画了什么了，莫名问了一句：殷姑娘今年多大了。</p><p>她的手鬼使神差地搭在了殷伏黎的手上。</p><p>就这样，虞妙典和殷伏黎建立了联系。</p>
