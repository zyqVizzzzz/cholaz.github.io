    <h2>第七章·仙踪</h2>
    <p>公苏谨团队现在有四个人：<a class="mark">公苏谨、西山雀、齐武子、虞妙哉</a>。虞妙哉还有个搭档，<a class="mark">薛巧人</a>，实际上是有五个人。他们各有分工，公苏谨统揽全局，西山雀负责寻找殷伏黎的下落，齐武子负责调查吴坦尔案，薛巧人尝试打入瞳朦子内部，虞妙哉给其他人提供必要的援助。</p><p>通过对比多起和瞳朦子相关的杀人案，公苏谨推断：策划谋杀吴坦尔的人，很有可能就是整起事件的策划者。他让齐武子先沿着这个方向查，去洛阳府翻翻卷宗，看看能否从中找到一些有价值的信息。</p><p>在此期间，齐武子结识了洛阳府的陈谊，两人走得很近。齐武子发现，陈谊的案卷条理清晰，环环相扣，可每到关键部分，要么就是物证断了，要么就是人证失踪，根本进行不下去，最后随便找了个老实人，屈打成招。陈谊说：我们也不想冤枉好人，但上面要求我们这样做，除了奉命办事，我们还有其他选择吗？</p><p>齐武子近两天都住在捕风馆，白天办案、探听消息，晚上在捕风馆听艺术家们聊天吹牛。少了推云士子的暴论横议，洛阳名士圈就像死了一样无聊，大家聊天的内容又回到了品藻、女人、丹药和玄学上。</p><p>齐武子从不参与讨论，他就坐在远处看着，因为公苏谨嘱咐过他办事要低调。大家都以为齐武子是司天台派来监视虞妙典的御史，所以也没人和他聊天<span class="tip">（司天台的名声很差，和东厂、克格勃、盖世太保差不多）</span>。</p><p>一天，陈谊派小厮给齐武子同步了一则消息：沈纨在监狱里交代了殷伏黎的住所，她住在外城角楼的阁楼上。齐武子立即和虞妙典一起动身前往。</p>
