    <h4>洛阳府</h4>
    <p>
      虽然赵王集团计划得很好，但大理寺作为中立机构，对站边皇后还有很多顾虑，一旦如此，他们就必须要罗织罪名、严刑逼供，必须要和太子党彻底撕破脸。太子党如果就此倒台则皆大欢喜。可太子党不是软柿子，不可能坐以待毙，如果事情办砸了，让太子翻盘，那太子党势必会加倍报复大理寺。大理寺不想当炮灰，就决定把得罪人的审问工作交给洛阳府。他们只负责调查，然后给洛阳府提供一个定罪方向。
