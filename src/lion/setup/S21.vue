    <p>对虞妙典来说，殷伏黎长相普通，身份低微，没什么吸引力，约会几次之后，她就对殷伏黎失去了兴趣。最后一次见面时，她对殷伏黎说：以后不用那么频繁的见面，保持一点新鲜感。殷伏黎没说什么。</p><p>第二天，虞妙典发现，她的随身符咒失窃了，这件事如果被司天台发现，她会受到很严厉的处罚，于是警觉起来，很快就锁定了嫌疑犯：殷伏黎。</p><p>当天下午，她在鹩儿巷抓到殷伏黎，质问她缘何盗窃，殷伏黎耍横道：欲盗则盗，你能奈我何？虞妙典揪起殷伏黎的后衣领，就像拎起一只不听话的猫，厉声道：偷去干嘛呢？想做坏事？殷伏黎嘴硬道：做好事。</p><p>虞妙典说：看你这不知天高地厚的样子。</p><p>虞妙典把殷伏黎强行拖回捕风馆：废话少说，把我的东西交出来。殷伏黎说：用掉了。虞妙典不再纠缠，直接把手伸进殷伏黎的衣服里，果然搜出几张符纸，但还少了几张，虞妙典问：还有呢？</p><p>殷伏黎说：我说过了，用掉了。</p><p>虞妙典说：不见棺材不掉泪是吧。</p><p>说完便对殷伏黎施展瞳术：虞妙典的瞳孔骤然收缩，如利剑出鞘，寒芒四射。而殷伏黎则感到周遭的空间极速扭曲，五感也被某种不可阻挡的力量封闭住了。她无法呼吸，溺水般的窒息感汹涌袭来。</p><p>虞妙典开始倒计时，殷伏黎的听觉被打开了，从十数到一，她数得很慢，因为要检查殷伏黎的记忆，检查她是否用过符咒，怎么用的。</p><p>漫长的倒计时终于结束了，殷伏黎瘫倒在地，涕泗横流。虞妙典用无忧眼看到了很多意想不到的景象，于是蹲身揪起殷伏黎的头发，饶有兴味地问：且不论偷窃之事，你告诉我，是你杀了许无垠吗？</p><p>殷伏黎见识到了虞妙典的厉害，低着头，不敢与她对视：姐姐冤枉，我根本不认识许无垠。</p><p>虞妙典再次发动无忧眼，此次时间更长，直到殷伏黎意识模糊，眼神失焦，才肯解除，她问：是不是你杀了许无垠？</p><p>殷伏黎不想再受苦，边哭边说：如果我说了，你会放我走吗？</p><p>虞妙典的瞳孔又开始收缩，殷伏黎连声说：我知道了，我说我说。</p><p>虞妙典说：想清楚，别说错了。</p>
    <h3>杀死一个雕刻匠</h3>
    <h4>零阶段：打工人的苦恼</h4>
    <p>
      许无垠本来是个温文尔雅的人，对丹青理论研究颇深，而且擅长各种手工，是个岁月静好的文艺青年。但他最近压力很大，因为王君夫输了斗富竞赛，便把情绪宣泄在下属身上。寺山水院的领导为了讨好王君夫，要求画馆成员反复研究陆小乙的画作，并在每天下班之前上交一篇攻谄文章。
    </p>
    <p>
      许无垠对陆小乙的画作毫无兴趣，却终日都要面对着这幅处处透露着诡谲的水府流云图，他试过应付交差，可等着他的却是领导无穷无尽的PUA，有一次，他没忍住说了几句心里话，在文章里隐晦地夸赞了陆小乙的画技，还对陆小乙团队高超的颜料制作技术表示惊叹。
    </p>
    <p>第二天他就被领导痛骂了一顿。这份工作让他非常难受。</p>
    <h4>阶段一：养成</h4>
    <p>
      他开始流连于烟花柳巷，每天下班后，他都会去鹩儿巷缓解压力。以前没感觉，但现在，这里成了他最后的避难所。
    </p>
    <p>
      他在这里结识了白伊蔚，两人很投缘。白伊蔚对他的雕刻艺术很欣赏，她说：陆小乙在成名前也有过很久的灰暗时光，你早晚也会成功的。
    </p>
    <p>
      许无垠听到陆小乙，好心情荡然无存，他沮丧地说：我怕是躲不掉他了，为什么到处都是他的名字？白伊蔚说：因为他是我们四夷坊的人，我们眼看着他从无名小卒走到今天，我们都是他的「嫡系」拥护者。
    </p>
    <p>
      许无垠问：他到底是个怎样的人呢？白伊蔚斩钉截铁地说：一个大好人。许无垠不相信：可我听到的都是关于他不好的传闻。白伊蔚说：批评他的都是上流人物吧，可能大家对好人的标准有所不同。
    </p>
    <p>
      许无垠问：你能看懂他的画吗？白伊蔚说：水府流云图我看不懂，但他给胡炼画的仕女图真漂亮。许无垠说：其实我也是个丹青客。白伊蔚说：你不是雕刻家吗？许无垠说：以前是丹青客。白伊蔚说：我不信。许无垠说：不如我也给你画一幅仕女图，这样你以后就不用羡慕别人了。
    </p>
    <h4>阶段二：崩坏</h4>
    <p>
      他给白伊蔚画了几幅仕女图，白伊蔚很开心，嘴巴像抹了蜜一样甜，对他更是盛赞有加，而他也从中得到了很大的慰藉。
    </p>
    <p>
      <p>后来他又给白伊蔚雕刻人像。白天上班，写文章骂陆小乙，晚上去鹩儿巷，走陆小乙走过的艺术之路<span class="tip">（据说水府流云图就是在鹩儿巷诞生的）</span>。甚至他的精神状态也和当初的陆小乙如出一辙：他常常因为无法还原理想中的白伊蔚而感到苦闷。</p>
    </p>
    <p>
      许无垠的心态悄然变化，他在理解陆小乙的道路上越走越远，最终写出了让领导们满意的「论云」，他在文章中剖析了陆小乙在画作里隐藏的恐怖主义倾向。而这也恰恰是他本人的内心写照。
    </p>
    <p>
      有一天，他把白伊蔚叫到家里，质问道：我给你画的仕女图，为什么要说成是陆小乙画的？白伊蔚说：那是胡炼故意气我，我气不过才这么说的。许无垠说：陆小乙就是高贵的，我的名字就让你羞于启齿吗？白伊蔚说：我已经说过了，我是被胡炼气糊涂了，你到底要我说几次。许无垠冷静了一会儿，拿出一个小木雕，试图缓和气氛：你看，我又给你做了一个雕像，好看吗？白伊蔚吓得倒退两步——
    </p>
    <p>一个画风崩坏，中邪般的白伊蔚<span class="tip">（许无垠魔障了）</span>。</p>
    <h4>阶段三：占有和杀戮</h4>
    <p>
      从那以后，白伊蔚开始有意避开许无垠，但许无垠对她的占有欲却越发强烈。她去找殷伏黎诉苦，殷伏黎说她想多了，并对许无垠的艺术表示很感兴趣。
    </p>
    <p>
      又一天，许无垠邀请白伊蔚去他家里，白伊蔚委婉地拒绝了他，还告诉他，以后最好不要有太多私下见面。这激怒了许无垠，他把白伊蔚强行带回家里。他说：我的杰作里不能没有你，只有我才能刻画出你的美貌。
    </p>
    <p>
      他给白伊蔚服下超剂量的五夜丹，药效发作后，白伊蔚意识模糊，浑身无力，只能任其摆布。他搬出一套巨型木雕设备，对白伊蔚说：你的血、肉、灵魂，都会在今晚成为不朽的杰作，你会永远和我在一起。
    </p>
    <p>许无垠疯了，他要把白伊蔚肢解，做成木雕。</p>
    <p>
      他在白伊蔚的身体关节处画上各种记号，确认无误后，拿出一把锯子，放在白伊蔚脖颈关节处。他开始行动，鲜血顺着白伊蔚的脖子流淌下来，他的嘴里不断重复着：只有我才能刻画出你的美貌。
    </p>
    <p>
      就在这时，殷伏黎突然从虚空里一跃而出，手里拿着一块砖头，有如天神下凡，从背后拍倒了许无垠。
    </p>
    <h4>白伊蔚：死亡之吻</h4>
    <p>
      许无垠虽被击倒，但很快恢复了意识，起身反击。情急之下，殷伏黎抄起一根有尖刺的木条，向前乱挥，许无垠格挡不及，猛然被捅穿了喉咙，鲜血四溅。
    </p>
    <p>许无垠死了。</p>
    <p>
      殷伏黎给白伊蔚吃了「发散剂」，解除五夜丹的效力，又给她止了血。白伊蔚从没见过此等血腥场面，捡起衣服便要逃，殷伏黎说：你逃吧，街上全是金吾卫，你出去就被抓。白伊蔚大哭，口中喃喃道：何至于此啊。良久，白伊蔚说：我们躲开金吾卫，回去收拾细软，明天就离开洛阳吧。
    </p>
    <p>
      殷伏黎说：我们两个弱女子，能逃到哪里呢？明天许无垠的尸首被发现，洛阳府马上就会找到我们，我们走不远，也无处可去。不过你放心，纵然是死，我也会陪你一起死。
    </p>
    <p>白伊蔚说：我不会把你说出来的，我就说是我一人所为。</p>
    <p>
      殷伏黎帮白伊蔚擦掉眼泪，说：别异想天开了，你说不清的，且洛阳府手段狠毒，你必难承受。我既然来了，无论发生什么都会陪着你。白伊蔚说：是我害了你。殷伏黎说：不是你的错，是许无垠，是他害的。
    </p>
    <p>
      殷伏黎把另一根尖刺木条扔给白伊蔚：刺他几下，反正横竖都是一死，不如泄了恨再死。白伊蔚不从，殷伏黎骂道：废物，天生待宰的东西！殷伏黎把木条强塞在白伊蔚的手里，抓住她的手猛刺数下。
    </p>
    <p>白伊蔚说：逃也不能逃，我们难道就这么坐以待毙吗？</p>
    <p>
      殷伏黎说：与其被抓，狱中受辱，不如自裁。实话告诉你，我早有厌世之意，此生无趣，不如来世投胎到一个好人家，也胜过今生之苦。
    </p>
    <p>白伊蔚说：什么意思？</p>
    <p>
      殷伏黎忽然凑上近前，衔住白伊蔚的嘴唇，激吻。有那么一瞬间，白伊蔚觉得死亡好像也不是很可怕了，她推开殷伏黎：我决定了，这一切本就和你无关，你是为了救我，我绝不会连累你，你回去吧，我留在这里自行了断，明日官差发现，只道是我畏罪自杀。
    </p>
    <p>
      殷伏黎说：你傻吧，我为了找你，偷了司天台的符咒，我亦难逃。我不是告诉过你吗？我既然来了，死也要和你一起死。
    </p>
    <p>
      白伊蔚和殷伏黎相拥而泣，殷伏黎的情义让她不再胆怯。于是她们各自撕开自己的衣服，结成两根绳子，吊在梁上，携手赴死了。
    </p>
    <p>
      一炷香后，白伊蔚气绝身亡，殷伏黎却从梁上掉了下来。她没死，躺在地上眩晕良久，终于确认自己还活着——
    </p>
    <h3>猎物</h3>
    <p>虞妙典指着殷伏黎说：你骗了她。</p>
    <p>
      殷伏黎说：苍天在上，我绝没有，你尽可以去鹩儿巷打听，我殷伏黎是什么人，如果我有骗她的想法，我何必冒险救她呢？虞妙典说：你还活着，就证明你在骗她。殷伏黎说：我只是不敢再死一次了，死太难受了。
    </p>
    <p>
      说得通，但虞妙典隐隐觉得不对劲，却一时想不出问题在哪，就抛出一个新问题：你偷走我的白符没几天，它就派上用场了，世上会有这样的巧合？殷伏黎说：这不是巧合，我不止有一张白符。
    </p>
    <p>虞妙典说：哦？</p>
    <p>
      殷伏黎说：你说的白符，陆小乙家里就有，他不知道这些东西是做什么用的，就被我拿走了，我只知道这些是符，但绝不知道是司天台的符。后来在四通市听梅花道长吹牛，才知道它的来头，前些天我在你的床下发现同样的符纸，实在难忍好奇，所以才偷偷拿走了几张。
    </p>
    <p>虞妙典说：他家里除了白符，还有其他的符吗。</p>
    <p>
      殷伏黎说：没有了，我们是在很多年前，整理他爸爸遗物时发现的，那时我和沈纨在典当铺工作，对这些东西很感兴趣，就拿回去了<span
        class="tip"
        >（这句话是明显的谎言，因为她刚认识陆小乙没多久，但虞妙典不知道）</span
