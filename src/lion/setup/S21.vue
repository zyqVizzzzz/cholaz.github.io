    </p>
    <p>
      司天台的任务是把巫术和恐怖袭击罪名坐实，他们派出两个调查组，一组对水府流云图的巫术进行技术分析，由姜探微领衔；一组调查瞳朦子的起源和犯罪历程，由公苏谨领衔。
