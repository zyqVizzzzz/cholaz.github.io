    <p>
      调查瞳朦子，其实就是伪造证据，把瞳朦子的幕后主使指向殷伏黎和陆小乙，形成闭环。因为按照司天台高层的说法，瞳朦子的行动是一场无差别无组织的恐怖袭击，并不存在一个真正的幕后主使，所以追查瞳朦子是浪费时间，重点是把这口黑锅扔给太子党。在所有被捕人员里，殷伏黎是最适合背锅的那个。
