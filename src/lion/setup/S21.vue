    <p>
      殷伏黎的建议让陆小乙感到不安，画画时非但无法摒除杂念，反而被各种无由的疑虑所包围，以至于画笔狂乱，脱离本意，失其所控。忽然，屋外传来一阵少女的狂笑。
    </p>
    <p>胡炼见是殷伏黎，不客气地说：谁让你来的？这是我的房间，滚出去。</p>
    <p>
      殷伏黎没理她，对陆小乙说：陆公子怎么还在画胡炼，不是说好让我来接替她吗？陆小乙说：我几时说过？殷伏黎说：你当然说过，你说你厌倦胡炼了，这女人越看越蠢，只会毁掉你的灵感。
    </p>
    <p>
      胡炼说：陆小乙？陆小乙无奈地说：鬼话连篇。殷伏黎说：你还说，你与胡炼年少相识，却不料长大后竟变得如此不堪，你想要还原记忆里的小狐狸，可面前的人无论如何都只是个钻营的妓女。陆小乙怒道：放屁！信口雌黄！我从没这样说过！
    </p>
    <p>
      小狐狸是很久以前陆小乙给胡炼取的绰号，如果陆小乙什么都没说，殷伏黎就不可能知道。而且就算他没说，他心里也是这么想的。胡炼很难受，便赌气道：不画了，累了，你走吧，以后也别来了。又对殷伏黎说：害人精，你别犯在我手上。
    </p>
    <p>
      回去的路上，殷伏黎一直跟着他，说：你看，我说胡炼很蠢吧。陆小乙说：你无中生有，故意挑事，你意欲何为呢？殷伏黎直截了当地说：画我。陆小乙说：凭什么？殷伏黎说：看看我的脸。
    </p>
    <p>
