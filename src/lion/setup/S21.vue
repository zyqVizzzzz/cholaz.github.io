    </p>
    <p>虞妙典说：除了你，你的那个同伙也知道白符的事情吗？</p>
    <p>殷伏黎说：沈纨，他不知道，他只是个收当的，对方术一窍不通。</p>
    <p>虞妙典说：听起来你很精通？</p>
