    <p>
      陈谊说：尽快，凑够之后，去东市换十七枚「东华五铢」，我便知道你准备好了。夜里子时，我在清明门外的城隍庙等你。我拿到钱之后，你就安心回家，剩下的事交给我。
    </p>
    <p>
      吴坦尔有些不放心：这，你若拿了钱走，我又该找谁？陈谊说：吕狄向你说了我以前的事情，想必你自己也核实过了，我当年杀主，分文未受，全凭道义二字。如今曹芳无德，害宁蓉家破人亡，我必取之，你尽可放心。吴坦尔说：二百两目前对我来说实在太多，不如我先奉上七十两，事成之后再补足余额。我们是同一条绳上的蚂蚱，你出事就是我出事，我无论如何也不敢赖账的。陈谊想了想，说：也罢，事成之后，你尽快筹钱，等我来找你。但切记不要声张，不要向亲友借钱，否则洛阳府就会查到你身上。另外，一定要按我说的，去东市换十七枚东华五铢，不能多也不能少。夜里去城隍庙，我若不在，你就把钱藏在城隍庙某处，我自会找到。
    </p>
    <p>吴坦尔说：了然。</p>
    <p>
      吴坦尔筹到钱，按要求换了十七枚东华五铢，前往城隍庙。陈谊不在，他想把钱放进井里，又怕拿不上来，藏在神龛后面，又怕被人发现，想来想去，决定在后院挖个坑埋起来，陈谊是办案的官差，应该不难发现他破土藏金的痕迹。
    </p>
    <p>
      就在吴坦尔挖坑藏金之际，背后忽然寒光一闪，未及反应，他便被刀子捅开了花。
    </p>
    <p>
      行凶者是吕狄，而陈谊还在赶往城隍庙的路上。吕狄杀了吴坦尔之后，从怀里取出一把菟丝草，放在土坑里，点燃，又用手指沾了点血，在墙上留下陆小乙的三句诗，最后扬长而去。走到半路，又折返回来，银子忘拿了。
    </p>
    <p>
      陈谊来到城隍庙，见吴坦尔死了，心凉半截。他强作镇定，简单查看了一下尸体身上的刀口：竟然是吕狄所为。陈谊立即回家，此时，吕狄正在清洗身上的血迹，陈谊二话不说，扑了上去，锁住吕狄的喉咙，问他：谁让你害我的？
    </p>
    <p>吕狄艰难地说：我没有。</p>
    <p>陈谊不再多问，他杀心已起，果断下死手。</p>
    <p>
      这时，房间里传来一个熟悉的声音：八年不见，你怎么还是这么冲动？没长进呢。
    </p>
    <p>穆千穗从里面走了出来：吕伯言要死了，放开他。</p>
    <p>
      八年不见，穆千穗的气场比以前强大得多，她带着阴森而又妖媚的金丝面具，遮住半张脸，而另外半张脸仍和少年时代一样，清澈无辜。这种反差让陈谊感到恐惧，他瞬间泄了气：是你？你、你对吕狄做了什么？
    </p>
